text.startsWith("Hello");
console.log(text.startsWith("Hello")); // Output: true
// The startsWith() method determines whether a string begins with the characters of a specified string.
// Syntax: string.startsWith(searchString, position)
// Parameters:
// - searchString: The string to search for at the start of the string.
// - position: Optional. The position in the string at which to begin searching for searchString. Default is 0.
// Return value: A boolean, indicating whether the string starts with the specified characters.
