let text = "John Doe";
text.endsWith("Doe");
console.log(text.endsWith("Doe")); // Output: true
// The endsWith() method determines whether a string ends with the characters of a specified string.
// Syntax: string.endsWith(searchString, length)
// Parameters:
// - searchString: The string to search for at the end of the string.
// - length: Optional. The length of the string to consider for the search. Default is the entire string.
// Return value: A boolean, indicating whether the string ends with the specified characters.
