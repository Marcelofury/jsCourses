let text = "Hello world!";
let result = text.repeat(4);
console.log(result); // Output: Hello world!Hello world!Hello world!Hello world!
// The repeat() method constructs and returns a new string which contains the specified number of copies of the string on which it was called, concatenated together.
// Syntax: string.repeat(count)
// Parameters: count - The number of times to repeat the string. If count is negative, zero, or Infinity, a RangeError is thrown.
// Return value: A string, representing the repeated string.
